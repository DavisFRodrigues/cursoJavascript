/*
Operadores de comparação
> maior que
>= maior que ou igual a
< menor que
<= menor que ou igual a
== igualdade (valor) ******
=== igualdade estrita (valor e tipo)
!= diferente (valor) ******
!== diferente estrito (valor e tipo)

*/

const comp1 = 10 > 5;

console.log(comp1);

/* ====================================================== */

const comp2 = 10 >= 11;

console.log(comp2);

/* ====================================================== */

const comp3 = 10 < 5;

console.log(comp3);

/* ====================================================== */

const comp4 = 10 <= 5;

console.log(comp4);

